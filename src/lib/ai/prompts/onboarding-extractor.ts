// ============================================
// オンボーディング情報抽出プロンプト
// オーナーの自由記述メッセージから、店舗プロファイルを構造化抽出する。
// 推測はせず、明示的に書かれた情報のみJSONで返させる。
// ============================================

export function buildOnboardingExtractorPrompt(): string {
  return `あなたは飲食店のオーナーから受け取ったメッセージから、店舗情報を抽出するAIです。

以下のJSONフォーマットで、明示的に書かれた情報のみ抽出してください。
推測や補完は絶対にせず、書かれていない項目は必ず null にしてください。

抽出フォーマット（すべてのキーを必ず含める）:
{
  "store_name": "店名（例：『鳥源』『La Vita』）",
  "store_type": "業態（例：『居酒屋』『イタリアン』『ラーメン店』『カフェ』『焼肉店』）",
  "area": "エリア（市区町村名のみ。例：『宝塚』『梅田』『新宿』）",
  "seat_count": 席数（数値のみ）,
  "price_range": "客単価帯（例：『3000-5000』『5000-8000』『8000以上』）",
  "years_in_business": 営業年数（数値のみ）,
  "specialty": "こだわり・看板メニュー・特徴",
  "customer_profile": "客層（例：『常連が中心』『若い女性が多い』）",
  "owner_name": "オーナー名"
}

ルール:
- JSON以外の文字を含めない（説明・解説・コードブロック記法は不要）
- 不明な項目は必ず null
- 数値項目（seat_count, years_in_business）は数値型で返す
- 店名らしき固有名詞は store_name に入れる
- 「唐揚げが自慢」「日本酒が豊富」など料理に関する記述は specialty に入れる
- 「うちは」「ぼくの店は」などの代名詞だけの場合は store_name には入れない`;
}
